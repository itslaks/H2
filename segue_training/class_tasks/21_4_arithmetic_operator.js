let a = 10;
let b = 3;

console.log("a = 10, b = 3");

// 1. + (Addition)
// Adds a and b
let sum = a + b;
console.log("a + b ➜", sum); // 13

// 2. - (Subtraction)
// Subtracts b from a
let difference = a - b;
console.log("a - b ➜", difference); // 7

// 3. * (Multiplication)
// Multiplies a and b
let product = a * b;
console.log("a * b ➜", product); // 30

// 4. / (Division)
// Divides a by b
let quotient = a / b;
console.log("a / b ➜", quotient); // 3.333...

// 5. % (Modulus)
// Remainder when a is divided by b
let remainder = a % b;
console.log("a % b ➜", remainder); // 1

// 6. ** (Exponentiation)
// a raised to the power of b
let power = a ** b;
console.log("a ** b ➜", power); // 1000

// 7. ++ (Increment)
// Increases a by 1 (can be pre or post)
let c = 5;
console.log("c = 5");
console.log("c++ ➜", c++); // 5 (returns old value, then adds 1)
console.log("After c++ ➜", c); // 6
console.log("++c ➜", ++c); // 7 (adds 1 first, then returns)

// 8. -- (Decrement)
// Decreases a by 1 (can be pre or post)
let d = 5;
console.log("d = 5");
console.log("d-- ➜", d--); // 5
console.log("After d-- ➜", d); // 4
console.log("--d ➜", --d); // 3
